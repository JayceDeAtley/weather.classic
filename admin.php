            <h3>About NOAA's National Weather Service</h3></td>
        </tr>
        <tr>
          <td valign="top"><p>The National Weather Service is a component of
              the National Oceanic and Atmospheric Administration (NOAA). NOAA
              is an Operating Unit of the U.S. Department of Commerce.</p>
            <p> <a name="top" id="top"></a> </p>
            <ul>
              <li> <a href="#mission">What is the mission of NOAA's National
                  Weather Service? </a>
                <ul>
                  <li>Mission and Organization of the Department of Commerce </li>
                </ul>
              </li>
              <li><a href="#sp">What are the long range plans and goals of the
                  National Weather Service?</a>
                <ul>
                  <li>NOAA Strategic Plan</li>
                  <li>Department of Commerce Strategic Plan </li>
                </ul>
              </li>
              <li><a href="#organization">How is the National Weather Service
                  organized?</a></li>
              <li><a href="#foia">Freedom Of Information Act (FOIA) requests </a></li>
              <li><a href="#business">Commerce and the Small Business Paperwork
                  Reduction Act (SBPRA)</a></li>
              <li><a href="#no_fear">Notification and Federal Anti-Discrimination
                  and Retaliation (NO FEAR) Act of 2002</a></li>
              <li><a href="#careers">What jobs are available in the National
                  Weather Service, and how do I apply?</a></li>
              <li><a href="#link">Can I link to your site? Can you add a link
                  to my site? What is your linking policy? </a> </li>
            </ul>
            <p name="mission"><a name="mission" id="mission"></a><strong>The
                Mission of NOAA's National Weather Service </strong></p>
            <p> The National Weather Service (NWS) provides weather, hydrologic,
              and climate forecasts and warnings for the United States, its territories,
              adjacent waters and ocean areas, for the protection of life and
              property and the enhancement of the national economy. NWS data
              and products form a national information database and infrastructure
              which can be used by other governmental agencies, the private sector,
              the public, and the global community. </p>
            <ul>
              <li><a href="http://204.193.232.34/cgi-bin/doit.cgi?204:112:ca6ff65e72b6f38e4fe42ea2ba9cf9358ad6df26eae063a68a6e8f420a9ac897:245">Mission
                  and Organization of the Department of Commerce</a> </li>
            </ul>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p><a name="sp" id="sp"></a><strong>NWS Strategic Plan</strong></p>
            <p> The Strategic Planning and Policy Office provides support to
              the Assistant Administrator for Weather Services and Deputy Assistant
              Administrator through the development and implementation of an
              integrated approach to NWS policy, strategy, and long-range planning
              processes. The Office develops NWS-wide long-range policy objectives
              and develops and manages the NWS strategic planning process through
              implementation. The Office develops and recommends policy on NWS
              and private sector roles, activities, and relationships </p>
            <ul>
              <li><a href="http://www.weather.gov/sp/">National Weather Service
                  Strategic Plan</a></li>
              <li><a href="http://www.spo.noaa.gov/">NOAA's Strategic Plan</a></li>
              <li><a href="http://www.osec.doc.gov/bmi/budget/budgetsub_perf_strategicplans.htm">Department
                  of Commerce Strategic Plan</a> </li>
            </ul>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p align="left"><a name="organization" id="organization"></a><strong>National
                Weather Service Organization</strong></p>
            <p>The <a href="http://www.weather.gov/hdqrtr.php">National Headquarters
                of NOAA's National Weather Service</a> is located in Silver Spring
                Maryland. Headquarters - </p>
            <p>Regional Headquarters, National Centers, Weather Forecast Offices
              and River Forecast Centers and Field Offices are located across
              the country. Links to web sites of these components of the National
              Weather Service are <a href="http://www.weather.gov/organization.php">listed
              here</a>.</p>
            <p> <a href="http://weather.gov/organization.php#local"> Forecast
                Offices</a> | <a href="http://weather.gov/organization.php#regional">Regional
                Support Centers</a> | <a href="http://weather.gov/organization.php#support">National
                Support Centers</a></p>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p><a name="foia" id="foia"></a><strong>How do I make a request for
                information under the Freedom Of Information Act (FOIA)?</strong></p>
            <p>Information on the Freedom Of Information Act and how to submit
              a request is posted <a href="http://www.rdc.noaa.gov/%7Efoia/">here</a>. </p>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p><a name="business" id="business"></a><strong>Commerce and the
                Small Business Paperwork Reduction Act (SBPRA)</strong></p>
            <p> The Department of Commerce understands that a vibrant small business
              sector is critical to creating new jobs in a dynamic and growing
              economy, and provides substantial assistance through a variety
              of media to help small businesses comply with enforcement regulations
              and paperwork requirements.</p>
            <p>Department of Commerce operating units with paperwork requirements
              that affect small businesses have established Web sites, e-mail
              addresses, and toll free phone numbers dedicated to assisting small
              businesses in responding to their paperwork requirements.</p>
            <p><a href="http://www.osec.doc.gov/cio/oipr/oipr/sbpra.htm">Commerce
                and the Small Business Paperwork Reduction Act (SBPRA) Contact
                Points</a></p>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p><a name="no_fear" id="no_fear"></a><strong>Notification and Federal
                Anti-Discrimination and Retaliation (NO FEAR) Act of 2002 compliance?</strong></p>
            <p> On May 15, 2002, President Bush signed legislation called the
              No FEAR Act (Notification and Federal Anti-Discrimination and Retaliation
              Act of 2002). This act, which took effect on October 1, 2003, makes
              Federal agencies individually accountable for violations of anti-discrimination
              and whistleblower protection laws. </p>
            <ul>
              <li><a href="http://www.osec.doc.gov/ocr/nofear/nofearnoaa.htm">No
                  FEAR Act EEO Complaint Data</a></li>
            </ul>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p><a name="careers" id="careers"></a><strong>What jobs are available
                in the National Weather Service, and how do I apply?</strong></p>
            <p>The National Weather Service offers a variety of career opportunities.
              Vacancy announcements and information on how to apply for positions
              as meteorologists, computer specialists, hydrologists are posted
              on our <a href="http://www.weather.gov/careers.php">Careers Page</a>.
              There are also a number of opportunities for students, these programs
              are described on our <a href="http://www.weather.gov/eeo/StudentResearchOpportunities.htm">Student
              Employment Page</a>. </p>
            <p align="right"><a href="#top">Top of Page</a></p>
            <p>&nbsp;</p>
            <p><a name="link" id="link"></a><strong>Can I link to your site?
                Can you add a link to my site? </strong></p>
            <p>The information on National Weather Service Web servers and Web
              sites is in the<b> public domain</b>, unless specifically annotated
              otherwise, and may be used freely by the public. Before using information
              obtained from this server special attention should be given to
              the <b>date &amp; time</b> of the data and products being displayed.
              This information shall not be modified in content and then presented
              as official government material. More information about linking
              to our pages and using NWS data is contained in our <a href="http://www.weather.gov/disclaimer.php">Disclaimer</a>. </p>
            <p>Our policy on linking from National Weather Service sites states
              that links must directly relate to the mission of the National
              Weather Service, and the information or data not be available from
              other Federal Internet sources. Our Linking Policy is contained
              within the National <a href="http://www.nws.noaa.gov/directives/sym/pd06001curr.pdf">Weather
              Service Directive 60-1 </a>(PDF format) </p>
