<h1><i>Current MOS Forecast Products</i></h1>
<i>This web site is not monitored 24/7 so the files on this server may not always be the most recent.</i><br>
<br>
<b>Model Output Statistics (MOS) is a technique used to objectively <br>interpret numerical model output and produce site-specific guidance.</b>
<p>The text, grib, and bufr messages are always current on the ftp server.  Links are provided below.  Select the desired date and cycle from the directory listing.</b></p>
<p><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/">FTP link to full text messages</a>
<br><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/DS.mav">FTP link to full GFS short-range text messages</a>
<br><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/DS.mex">FTP link to full GFS extended-range text messages</a>
<br><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/DS.mcg">FTP link to full GFS short-range coop max/min text messages</a>
<br><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/DS.mcx">FTP link to full GFS extended-range COOP max/min text messages</a>
<br><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/DS.met">FTP link to full NAM text messages</a>
<br><a href="ftp://tgftp.nws.noaa.gov/SL.us008001/DF.anf/DC.mos/DS.fwc">FTP link to full NGM text messages</a>
<br>
