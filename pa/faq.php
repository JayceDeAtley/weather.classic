            Wooden legs: 48"L (1.2m) </p>
            <p>Weight/shipping: <br />
              Enclosure: 70lbs/75lbs (32kg/34kg) <br />
            Metal legs:10lbs/15lbs (4.5kg/6.8kg) </p>
