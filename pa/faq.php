            <p><strong><a name="q3" id="q3"></a>Q3. How do meteorologists calculate wind chill?</strong></p>
            <p><strong>A.</strong> The National Weather Service's Wind Chill chart, equation and calculator can found at <a class="moz-txt-link-freetext" href="http://www.weather.gov/om/windchill/index.shtml">http://www.weather.gov/om/windchill/index.shtml</a></p>
            <hr class="blue" />
            <p><a name="q4" id="q4"></a><strong>Q4. What are "degree days?"</strong> </p>
            <p><strong>A.</strong> Heating and cooling degree days are calculated by using the average temperature for the day, the high + low, and subtracting the average or mean temperature from the number 65 for heating degree days or subtracting 65 from the average for cooling degree days. The number 65 is used as the base number because most buildings and homes would be most comfortable and energy efficient at or around 65 degrees. </p>
            <p>Example: On a summer day the high was 90 and the low 70. The average is 80. 80 (average) - 65 (base) = 15 cooling degree days. It reflects how much power people should be using on the average to cool, in this case their homes/businesses. Take those daily cooling degree days and you can have daily/monthly or annual averages. </p>
            <p>Example: On a winter day the high was 40 and the low was 0. 65 (base) - 20 (average) = 45 heating degree days. </p>
            <hr class="blue" />
            <p><a name="q5" id="q5"></a><strong>Q5. What employment opportunities are available with the National Weather Service, and what would be the best kind of college courses to take for these jobs?</strong> </p>
            <p><strong>A5.</strong> Most occupations with the National Weather
              Service are either for people trained in meteorology, atmospheric
              sciences, climatology, hydrology or related fields. Jobs available
              with the NWS are posted at: <a href="http://jobsearch.usajobs.opm.gov/a9noaa.asp">http://jobsearch.usajobs.opm.gov/a9noaa.asp</a></p>
            <p>For more specifics on careers in meteorology and recommended college course work, check out the American Meteorological Society's web site at:<a href="http://atm.geo.nsf.gov/AMS/"> http://atm.geo.nsf.gov/AMS/</a> </p>
            <p>The National Weather Association web site also lists information on careers in meteorology: <a href="http://www.nwas.org/jobs.html">http://www.nwas.org.html</a> </p>
            <p>The AMS has extensive career information on their site: <a href="http://www.ametsoc.org/atmoscareers/index.html">http://www.ametsoc.org/atmoscareers/index.html</a> </p>
            <hr class="blue" />
            <p><a name="q6" id="q6"></a><strong>Q6. How can I "tune-in" NOAA Weather Radio? How can I find out what frequency the station in my area is broadcasting at?</strong> </p>
            <p><strong>A6.</strong> You can find the location and frequency of your nearest NOAA Weather Radio transmitter by using the index located on the NWS Home Page at: <a href="http://www.nws.noaa.gov/nwr/nwrbro.htm#nwrstations">http://www.nws.noaa.gov/nwr/nwrbro.htm#nwrstations</a> </p>
            <hr class="blue" />
            <p><a name="q7" id="q7"></a><strong>Q7. Is there an archive, history, whatever for exact location (latitude and longitude) of tornadoes that have been cited, reported, measured?</strong> </p>
            <p><strong>A7.</strong> The NWS Storm Prediction Center tornado data archive contains latitude/longitude information for tornado segments (tornadoes crossing county lines are broken into one segment per county), for the years 1950-1995, by state or territory. This information can be accessed on the internet at: <a href="http://www.spc.noaa.gov/archive"> http://www.spc.noaa.gov/archive</a> </p>
            <hr class="blue" />
            <p><a name="q8" id="q8"></a><strong>Q8. Is there somewhere I can purchase weather equipment to make my own weather observations?</strong> </p>
            <p><strong>A8.</strong> Entering &quot;weather equipment&quot; into an Internet search engine should yield many companies that sell thermometers, anemometers, rain gauges and much more.</p>
            <hr class="blue" />
            <p><a name="q9" id="q9"></a><strong>Q9. Is there any way to find out which Weather Forecasting Office serves my area?</strong> </p>
            <p><strong>A9.</strong> Go to the following Web site and click on your area on the map of the United States to go to the NWS Forecast Office in your area: <a href="http://www.weather.gov">http://www.weather.gov</a> </p>
            <hr class="blue" />
            <p><a name="q10" id="q10"></a><strong>Q10. Can I visit my local NWS office?</strong> </p>
            <p><strong>A10.</strong> Visiting your local NWS Forecasting Office is something that is not only allowed, but encouraged. Unfortunately, there are times when this might not be possible. As you know, NWS offices are very busy places, especially during severe weather events. However, we understand that many people may want to have access to the offices at those times. The Internet has made it possible for our offices to distribute critical information to a wide audience quickly. </p>
            <p>Even though no office can permit unlimited access to their facility, especially during severe weather events when the potential for disruptions to operations is high, some offices can arrange for people to visit their offices or to observe severe weather operations on a limited basis. It would be necessary for you to clear any visits you wish to make with the warning coordination meteorologist (WCM) or meteorologist in charge (MIC) of the specific office that interests you. </p>
            <p>The NWS is usually able to accommodate an individual's request to see the office, but would like to set this set up in advance as much as possible. We can often accommodate unannounced "walk-ins", but they are at the visitor's "own risk". The NWS does not schedule formal tours from March 15 through June 15th because of severe weather demands. </p>
            <hr class="blue" />
            <p><a name="q11" id="q11"></a><strong>Q11. What is Skywarn and is there an internet site where I can get information about Skywarn and the services offered?</strong> </p>
            <p><strong>A11.</strong> Skywarn is the National Weather Service (NWS) program of trained volunteer severe weather spotters. Skywarn Spotters support their local community and government by providing the NWS and there local emergency managers with timely and accurate severe weather reports. These reports, when integrated with modern NWS technology, are used to inform communities of the proper actions to take as severe weather threatens. For more details on the Skywarn program, go to: <a href="http://www.skywarn.org/">http://www.skywarn.org/</a> </p>
            <hr class="blue" />
