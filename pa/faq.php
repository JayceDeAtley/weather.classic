            <p><a name="q14" id="q14"></a><strong>Q14. Is there a UV Index on the internet?</strong> </p>
            <p><strong>A14.</strong> The Ultraviolet Index is available at a couple of different sites. These are: <br />
              <a href="http://iwin.nws.noaa.gov/iwin/us/ultraviolet.html">http://iwin.nws.noaa.gov/iwin/us/ultraviolet.html</a> <br />
            <a href="http://www.cpc.ncep.noaa.gov/products/stratosphere/uv_index/uv_current.html">http://www.cpc.ncep.noaa.gov/products/stratosphere/uv_index/uv_current.html</a> </p>
            <hr class="blue" />
            <p><a name="q15" id="q15"></a><strong>Q15. What is a good location to find Television Weather information?</strong> </p>
            <p><strong>A15.</strong> Television Weather sites exist on the Internet in many locations. One of the really comprehensive sites available on the Internet now is the TVWeather site. It includes timely updates, graphics, and even real-time satellite images. The address to this site is: <a href="http://www.tvweather.com/">http://www.tvweather.com/</a> </p>
            <hr class="blue" />
            <p><a name="q16" id="q16"></a><strong>Q16. What is a "White-Out?"</strong> </p>
            <p><strong>A17.</strong> 'White-out' is the term used often to describe any condition during snowfall that severely restricts visibility. In the <em>Glossary of Weather and Climate</em> published by the American Meteorological Society, a whiteout is defined as &quot;an atmospheric optical phenomenon of the polar regions in which the observer appears to be engulfed in a uniformly white glow as a result of a lack of contrast between an overcast sky and an unbroken snow cover. Neither shadows, horizon, nor clouds are discernable; sense of depth and orientation is lost; dark objects in the field of view appear to &quot;float&quot; at an indeterminable distance.&quot; </p>
            <hr class="blue" />
            <p><strong><a name="q17" id="q17"></a>Q17. What is a "cotton region shelter" and are there NWS specifications and associated drawings for this item?</strong> </p>
            <p><strong>A18.</strong> The "Cotton Region Shelter" (it might be better known as an instrument shelter) is a wooden shelter built to stand approximately four feet above the ground and constructed with slats to allow for airflow but to protect instruments from precipitation. It normally housed thermometers (wet and dry bulb, max and min) and assorted other types of meteorological instruments. The shelter protected sensors and other instruments against errors and damage due to solar radiation, wind, and precipitation. It comes in several different models, stand along (large, small), and wall mounts. </p>
            <p>Size: 30"W x 20"D x 32"H (762 x 508 x 813)mm </p>
