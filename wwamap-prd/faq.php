            <p align="left"><strong><a name="new_colors" id="new_colors"></a>New
              Web Based Watch/Warning/Advisory Map Colors </strong> Why change the colors? The previous map process allowed only 30
              colors to be used. This caused complaints when we tried to use
              one color for several different watch/warning/advisory products.
              The new map process allows than 80 colors (one for each product).
              In theory, this allows us to convey more information to the users </p>
            <ul>
              <li> The new colors were basedlined from suggestions from web usablity
                experts. They are less &quot;glarey&quot; and toned down to be
                easier on the eyes;</li>
              <li> The increase of colors allowed products to be grouped by hue;
                For instance frost and freeze are blue, colors usually associated
                with the cold.</li>
            </ul>
            <p align="right"><a href="#top">Top of Page</a></p>
            <strong><a name="how_often" id="how_often"></a>How often does
            the map update?</strong> The map updates approximately every 5 minutes with all warnings,
            watches, advisories, and statements in effect.
            <p align="right"><a href="#top">Top of Page</a></p>
            <strong><a name="screen_res" id="screen_res"></a>When I click
            on the map for a forecast, I get a forecast for a completely
            different area. Why?</strong> The map pages were set up for optimized for either 4 X 3 (1.33)
            or 5 X 4 (1.25) aspects. Many new laptop screen resolutions are
            optimized for wide-screen DVD displays (16 X 9 or a 1.6 or 1.77
            aspect).
            <p> If you have recently purchased a new laptop with a wide screen
              and you find that you click in one area and get forecasts for
              a different location, you may want to check your screen resolution
              and set to one of the following: (Microsoft has provided instructions
              for checking or changing screen resolution on the <a href="http://www.weather.gov/cgi-bin/nwsexit.pl?url=http://www.microsoft.com/resources/documentation/windows/xp/all/proddocs/en-us/display_change_screen_resolution.mspx&amp;blurb=Microsoft+Windows+XP+Documentation">Microsoft
              Windows XP website</a>)<br />
              <br />
              800 X 600 (aspect 1.333) (SVGA) <br />
              1024 X 768 (aspect 1.333) <br />
              1152 X 864 (aspect 1.333) <br />
              1280 X 768 (aspect 1.25) (XGA) <br />
              1280 X 1024 (aspect 1.25) (SXGA) <br />
              1400 X 1050 (aspect 1.333) (SXGA+) <br />
              1600 X 1200 (aspect 1.333) (UXGA) <br />
              1600 X 1280 (aspect 1.25) <br />
              <br />
              Resolutions that are most likely to cause problems with the &quot;Point
              and Click&quot; interface of the WWA maps include: <br />
              1366 X 768 (aspect 1.778)(WXGA) <br />
              1680 X 1050 (aspect 1.6)(WSXCA+) <br />
              1920 X 1200 (aspect 1.778)(UXGA) </p>
